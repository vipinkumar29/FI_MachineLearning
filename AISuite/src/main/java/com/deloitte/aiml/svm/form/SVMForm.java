package com.deloitte.aiml.svm.form;

public class SVMForm{
	
	private String age;
	private String spectaclePrescrip;
	private String astigmatism;
	private String tearProdRate;
	private String contactLenses;
	
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getSpectaclePrescrip() {
		return spectaclePrescrip;
	}
	public void setSpectaclePrescrip(String spectaclePrescrip) {
		this.spectaclePrescrip = spectaclePrescrip;
	}
	public String getAstigmatism() {
		return astigmatism;
	}
	public void setAstigmatism(String astigmatism) {
		this.astigmatism = astigmatism;
	}
	public String getTearProdRate() {
		return tearProdRate;
	}
	public void setTearProdRate(String tearProdRate) {
		this.tearProdRate = tearProdRate;
	}
	public String getContactLenses() {
		return contactLenses;
	}
	public void setContactLenses(String contactLenses) {
		this.contactLenses = contactLenses;
	}

}